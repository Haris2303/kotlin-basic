fun main() {
    var age: Int = 1000
    var sample: Float = 5.5F

    println(age)
    println(sample)

    var binary: Int = 0b0101
    println(binary)

    var price: Long = 9_000_000_000L
    println(price)

    var priceInt: Int = price.toInt()
    println(priceInt)

    var doubleBinary: Double = binary.toDouble()
    println(doubleBinary)
}